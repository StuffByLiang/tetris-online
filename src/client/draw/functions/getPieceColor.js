window.draw.getPieceColor = function(piece) {
  switch(piece){
      case 1:
          return "#69BE28";
      case 2:
          return "#ED2939";
      case 3:
          return "#009FDA";
      case 4:
          return "#952D98";
      case 5:
          return "#0065BD";
      case 6:
          return "#FF7900";
      case 7:
          return "#FECB00";
      case 8:
          return "#696969";
  }
}
